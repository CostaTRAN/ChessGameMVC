package models;

/**
 * L'énumération Color représente les couleurs des pièces dans un jeu d'échecs.
 * Elle contient deux valeurs : WHITE et BLACK.
 */
public enum Color {
    /**
     * Représente la couleur blanche.
     */
    WHITE,

    /**
     * Représente la couleur noire.
     */
    BLACK
}
