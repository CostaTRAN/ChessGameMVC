package models;

/**
 * L'énumération PieceType représente les différents types de pièces dans un jeu d'échecs.
 */
public enum PieceType {
    /**
     * Représente un pion.
     */
    PAWN,

    /**
     * Représente une tour.
     */
    ROOK,

    /**
     * Représente un cavalier.
     */
    KNIGHT,

    /**
     * Représente un fou.
     */
    BISHOP,

    /**
     * Représente une reine.
     */
    QUEEN,

    /**
     * Représente un roi.
     */
    KING
}